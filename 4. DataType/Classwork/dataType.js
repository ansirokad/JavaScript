// 1. number : int, float

console.log(12);
console.log(13.5);
console.log(typeof (12));
console.log(typeof (13.5));

// 2. string : char, string

console.log('A');
console.log("Ram");
console.log(typeof 'A');
console.log(typeof "Ram");

// 3. Boolean 

console.log(true);
console.log(false);
console.log(typeof true);
console.log(typeof false);

// 4. Object : Array, Object, NULL

console.log([11,12,13]);
console.log({"fname" : "Ram"});

console.log(typeof [11,12,13]);
console.log(typeof {"fname" : "Ram"});

// array :

console.log(Array.isArray([11,12,13]));

// 5. Undefined : undefined

console.log(undefined);
console.log(typeof undefined);

// 6. Bigint

console.log(1234567890123456789n);
console.log(typeof 1234567890123456789n);

// 7. Function

console.log(console.log);
console.log(typeof console.log);
